"""
Use um loop "while" em Python para imprimir a mensagem 
enquanto o valor de x não for igual a 5. Aproveite para tirar todas as suas dúvidas.
"""

#Contador
contador = 0

#Criando loop para imprimir mensagem enquanto não for igual a 5 e soma 1 ao contador
while contador < 5:
    print(f'O número está abaixo de 5: {contador}')
    contador += 1

#Fim da execução
print('Fim da contagem')